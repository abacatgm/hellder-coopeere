<div class="tab">
  <h3 class="tab-title mt-2 mb-3">Informações de Preenchimento</h3>
  <p>Pedimos que use as funções a seguir durante o preenchimento:</p>
  <p>
    A função <strong>"Enviar"</strong> deverá ser utilizada somente quando todo formulário estiver preenchido e ao menos
    uma mensalidade for paga clicando no último item da barra de opções. Havendo necessidade de mudança nos dados
    enviados, clique na função "Reeditar" e todas as lacunas de preenchimento estarão aptas a serem modificadas. Um link
    será enviado para o e-mail ou um código para o celular. Siga as instruções.
  </p>
  <p>
    A função <strong>"Salvar"</strong> poderá ser utilizada a qualquer momento durante o preenchimento e assim garantir
    o que já foi preenchido não será perdido. Usando esta função, você poderá concluir o preenchimento novamente quando
    desejar.
  </p>
  <p>
    A função <strong>"Visualizar/Imprimir"</strong> encontrará o Formulário PRODIST padrão ANEEL pag. 73. Será neste
    formulário que enviaremos seus dados a Distribuidora de Energia responsável pelo seu município.
  </p>
  <p>
    A função <strong>"Reeditar"</strong> Clique em reeditar quando for fazer alterações nos dados já enviados a
    cooperativa. Será enviado um link para o e-mail usado no ato do cadastro. No caso de mudança de celular, será
    necessário o envio de link para o mesmo e-mail usado no cadastro. Mudança de e-mail enviaremos código via SMS para o
    celular usado no ato do cadastro.
  </p>
  <p>
    A função <strong>"Deletar conta"</strong>. Clique em deletar conta para remover todos seus dados e sair da
    cooperativa. Agradecemos se deixar um feedback sobre as razões de sua saída.
  </p>

</div>
